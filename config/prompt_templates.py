# 角色口癖模板（从设定包提取后在这里使用）
# ═══════════════════════════════════════════════
CHARACTER_VOICE_REMINDER_TEMPLATE = """
【角色口癖提醒（本章必须体现）】
{character_voices}

注意：口癖不是每段都用，而是在「角色情绪密度高」的时刻自然出现（每人每章1-2次）。
"""

def get_character_voice_reminder(characters: list, setting: dict) -> str:
    """从设定包提取角色口癖，生成提醒文本"""
    key_chars = {c["name"]: c for c in setting.get("key_characters", [])}
    mc = setting.get("protagonist", {})

    voice_lines = []
    # 主角
    mc_name = mc.get("name", "")
    if mc_name in characters and mc.get("speech_quirks"):
        quirks = " / ".join(mc["speech_quirks"][:2])
        voice_lines.append(f"{mc_name}：{quirks}")

    # 配角
    for char_name in characters:
        if char_name in key_chars:
            quirks = key_chars[char_name].get("speech_quirks", [])
            if quirks:
                voice_lines.append(f"{char_name}：{' / '.join(quirks[:1])}")

    if not voice_lines:
        return ""
    return CHARACTER_VOICE_REMINDER_TEMPLATE.format(
        character_voices="\n".join(voice_lines)
    )


def get_hook_guidance(hook_type: str) -> str:
    """获取指定钩子类型的写法指导"""
    hook = HOOK_TYPES.get(hook_type, {})
    if not hook:
        return f"结尾类型：{hook_type}"
    return (
        f"结尾钩子类型：{hook_type}\n"
        f"写法要点：{hook['pattern']}\n"
        f"参考示例：{hook['example']}"
    )


def get_genre_instruction(genre: str) -> str:
    """根据题材获取专属写作指令"""
    for key, instruction in GENRE_WRITING_INSTRUCTIONS.items():
        if key in genre:
            return instruction
    return GENRE_WRITING_INSTRUCTIONS["都市"]  # 默认都市
